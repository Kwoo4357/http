package com.studyLotto;


public class StudyMember{
    private String name;
    private int speakWeight;
    private int recordWeight;

    public String getName() { return name; }
    public void setName(String name) { this.name = name; }

    public int getSpeakWeight() { return speakWeight; }
    public void setSpeakWeight(int speakWeight) { this.speakWeight = speakWeight; }

    public int getRecordWeight() { return recordWeight; }
    public void setRecordWeight(int recordWeight) { this.recordWeight = recordWeight; }
}
